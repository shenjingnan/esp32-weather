void font8x16_draw_string(uint8_t *fb, int hor_res, int ver_res,
                          const char *str, int x, int y)
{
    while (*str) {
        unsigned char c = static_cast<unsigned char>(*str++);
        if (c < 32 || c > 126) {
            c = '?';  // 非 ASCII 字符替换为 '?'
        }
        const uint8_t *glyph = s_font8x16[c - 32];  // 16 字节点阵数据

        // 字体数据为水平行布局：glyph[row] 的 bit7..bit0 = 该行从左到右 8 个像素
        for (int row = 0; row < 16; row++) {
            int py = y - 15 + row;  // 从基线向上绘制
            if (py < 0 || py >= ver_res) continue;

            uint8_t row_data = glyph[row];
            for (int col = 0; col < 8; col++) {
                if (!(row_data & (1 << (7 - col)))) continue;  // MSB = 最左像素

                int px = x + col;
                if (px >= hor_res) break;

                // SSD1306 垂直页寻址格式
                int byte_idx = (py / 8) * hor_res + px;
                int bit_idx  = py % 8;
                fb[byte_idx] |= (1 << bit_idx);  // 设置像素为亮
            }
        }
        x += 8;  // 下一个字符的 X 偏移
    }
}
